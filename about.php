			<h2>Cupcake Variants</h2>

			<figure><img src = images/variants.jpg></figure>

			<p>A cake in a mug is a variant that gained popularity on many internet cooking forums and mailing lists. The technique uses a mug as its cooking vessel and can be done in a microwave oven. The recipe often takes fewer than five minutes to prepare. This variant has become popularised in recent years by the presence of numerous videos on social media websites, each claiming to detail the fastest method to create the finished product.</p>
			<p>A butterfly cake is a variant of cupcake, also called fairy cake for its fairy-like "wings". The top of the fairy cake is cut off or carved out, and cut in half. Then, butter cream, whipped cream or other sweet filling is spread into the hole. Finally, the two cut halves are stuck into the butter cream to resemble butterfly wings. The wings of the cake are often decorated using icing to form various patterns.</p>
			<p>A gourmet cupcake is a somewhat recent variant of cupcake. Gourmet cupcakes are large and filled cupcakes, based around a variety of flavor themes, such as Tiramisu or Cappuccino. In recent years there has been an upcropping of stores that sell only gourmet cupcakes in metropolitan areas. As an alternative to a plate of individual cakes, some bakers place standard cupcakes into a pattern and frost them to create a large design, such as a basket of flowers or a turtle.</p>
