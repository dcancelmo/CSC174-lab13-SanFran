		<section>
			<h2>Cappuccino Gourmet Cupcake</h2>

			<h3>Ingredients</h3>
			<ul>
				<li>1/2 cup butter, softened</li>
				<li>1/2 (8-oz.) package cream cheese, softened</li>
				<li>2 cups sugar</li>
				<li>4 large eggs</li>
				<li>1 teaspoon vanilla extract</li>
				<li>3 cups all-purpose flour</li>
				<li>1 teaspoon baking powder</li>
				<li>1/2 teaspoon baking soda</li>
				<li>1/2 teaspoon salt</li>
				<li>1 (8-oz.) container sour cream</li>
				<li>Double Shot Latte Buttercream</li>
				<li>Garnishes: instant espresso, ground cinnamon, and chocolate-covered coffee beans</li>
			</ul>

			<h3>Directions</h3>
			<ol>
				<li><p>Preheat oven to 350째. Beat butter and cream cheese at medium speed with an electric mixer until creamy. Beat in sugar until light and fluffy. Add eggs, 1 at a time, beating until blended after each addition. Stir in vanilla.</p></li>
				<li><p>Combine flour and next 3 ingredients. Gradually add to butter mixture alternately with sour cream, beating until blended. Spoon batter by rounded tablespoonfuls into lightly greased miniature cupcake tins.</p></li>
				<li><p>Bake at 350째 for 13 to 15 minutes or until a wooden pick inserted in centers comes out clean. Cool in tins on wire racks 5 minutes. Remove from tins to wire racks, and cool completely (about 30 minutes). Pipe or spread Double Shot Latte Buttercream onto cupcakes. Garnish, if desired.</p></li>
			</ol>

			<p>Note: To prepare regular-size cupcakes, spoon batter into 2 lightly greased 12-cup cupcake tins, filling two-thirds full. Bake at 350째 for 22 to 24 minutes or until a wooden pick inserted in centers comes out clean. Cool and decorate as desired. Makes 2 dozen.</p>
