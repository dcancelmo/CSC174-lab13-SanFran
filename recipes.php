			<h2>Butterfly Cake</h2>

			<h3>Ingredients</h3>
			<ul>
				<li>2 eggs</li>
				<li>1 cup self-raising flour</li>
				<li>1/2 cup butter/margarine</li>
				<li>1/2 cup Caster sugar</li>
				<li>2 teaspoons baking powder</li>
				<li>2 cups and 3 tablespoons icing sugar</li>
				<li>1/2 cup butter</li>
				<li>Milk</li>
			</ul>

			<h3>Directions</h3>
			<ol>
				<li><p>Preheat oven to 392째 for an eletric Oven and lay out 12 cupcake wrappers in a cupcake tin.</p></li>
				<li><p>Mix the sugar, flour and baking powder together. Add the butter/margarine and eggs and whisk all of it together.</p></li>
				<li><p>Using a teaspoon, fill the wrapppers with the mixture. Put the tray in the oven for around 15 minutes or until they are golden brown.</p></li>
				<li><p>Make the icing by whisking the butter and half the sugar together, once they are mixed add the rest of the sugar and a dash of milk, continue whisking.</p></li>
				<li><p>Use a knife to cut a upside-down, shallow cone shape out the top of the cake, cut this in half. Fill the hole in the cake with icing and then place the cut out cake on top (like wings.) Sprinkle some icing sugar on top of your cakes.</p></li>
